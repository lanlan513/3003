export function getRandomArtifact(siteId: string): { artifact: ExcavationArtifact; condition: ArtifactCondition; isShard: boolean } {
  const site = excavationSites.find(s => s.id === siteId);
  if (!site) throw new Error('Site not found');

  const isShard = Math.random() < site.shardWeight;
  const poolArtifacts = excavationArtifacts.filter(
    a => a.originSite === siteId && (isShard ? a.type === 'shard' : a.type === 'complete')
  );

  const fallbacks = excavationArtifacts.filter(a => a.originSite === siteId);
  const candidates = poolArtifacts.length > 0 ? poolArtifacts : fallbacks;

  const weighted = candidates.flatMap(a =>
    Array(Math.round(rarityConfig[a.rarity].weight * 100)).fill(a)
  );

  const artifact = weighted[Math.floor(Math.random() * weighted.length)] || candidates[0];

  const conditions: ArtifactCondition[] = isShard
    ? ['poor', 'poor', 'fair', 'fair', 'good']
    : ['fair', 'good', 'good', 'excellent', 'pristine'];
  const condition = conditions[Math.floor(Math.random() * conditions.length)];

  return { artifact, condition, isShard };
}

export function getRandomSite(): ExcavationSite {
  return excavationSites[Math.floor(Math.random() * excavationSites.length)];
}
